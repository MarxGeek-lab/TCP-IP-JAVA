package application.ClassAction.object;

public class AdresseList {

	private String adresse;
	

	public AdresseList(String ad) {
		this.adresse = ad;
	}
	
	public String getAdresse() {
		return adresse;
	}
	
	public void setAdresse(String value) {
		this.adresse = value;
	}
	
	public String toString() {
		return adresse;
	}
}
